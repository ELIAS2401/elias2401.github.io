const contenedorProductos = document.getElementById("contenedorProductos");
let botonesCategorias = document.querySelectorAll(".botonCategoria");
let tituloPrincipal = document.getElementById("tituloPrincipal");
let botonesAgregar = document.querySelectorAll(".botonEncargar");
let numerito = document.getElementById("numerito");

function cargarProductos(productosElegidos) {

    contenedorProductos.innerHTML = "";

    productosElegidos.forEach(producto => {

        const div = document.createElement("div");
        div.classList.add("productosCaja");
        div.setAttribute("name", "cajaProducto");

        div.innerHTML = `
            <div>
                <a href="#"><img src="${producto.imagen}" alt=""></a>
            </div>
            <div class="productoTexto">
                <p class="descripcion">${producto.titulo}</p>
                <p class="precio">$${producto.precio}</p>
                <div>
                    <button class="botonEncargar" id="${producto.id}">AÃ±adir al carrito</button>
                </div>
            </div>
        `

        contenedorProductos.appendChild(div);

    })

    actualizarBotones();
}

cargarProductos(arrayProductos);

botonesCategorias.forEach(boton => {

    boton.addEventListener("click", (e) => {

        botonesCategorias.forEach(boton => {
            boton.classList.remove("active");
        })

        e.currentTarget.classList.add("active");

        if (e.currentTarget.id != "todos") {
            const productosBoton = arrayProductos.filter(producto => producto.categoria.id === e.currentTarget.id);
            cargarProductos(productosBoton);
            tituloPrincipal.innerText = e.currentTarget.id;
        } else {
            cargarProductos(arrayProductos);
            tituloPrincipal.innerText = e.currentTarget.id;
        }
    });
});


function actualizarBotones() {
    botonesAgregar = document.querySelectorAll(".botonEncargar");
    botonesAgregar.forEach(boton => {
        boton.addEventListener("click", agregarAlCarrito)
    })
}

let productosEnCarritoLS = localStorage.getItem("productos-en-carrito");

let productosEnCarrito;

if(productosEnCarritoLS){
    productosEnCarrito = JSON.parse(productosEnCarritoLS);
    actualizarNumerito();
} else {
    productosEnCarrito = [];
}

function agregarAlCarrito(e) {

    const idBoton = e.currentTarget.id;
    const productoAgregado = arrayProductos.find(product => product.id === idBoton);

    // some devuelve true o false verifica que haya uno igual
    if (productosEnCarrito.some(producto => producto.id === idBoton)) {
        const index = productosEnCarrito.findIndex(producto => producto.id === idBoton);
        productosEnCarrito[index].cantidad++;
    } else {
        productoAgregado.cantidad = 1;
        productosEnCarrito.push(productoAgregado);
    }

    actualizarNumerito();

    localStorage.setItem("productos-en-carrito", JSON.stringify(productosEnCarrito));
}

function actualizarNumerito() {
    let nuevoNumerito = productosEnCarrito.reduce((acc, producto) => acc + producto.cantidad, 0);
    numerito.innerText = nuevoNumerito;
}